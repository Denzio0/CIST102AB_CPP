#include <iostream>
using namespace std;

int main () {
    int day = 1;

    switch (day){
        case 1:
            cout << "MON";
            break;

        case 2:
            cout << "TUES";
            break;

        case 3:
            cout << "WED";
            break;

        case 4:
            cout << "THURS";
            break;

        case 5:
            cout << "FRI";
            break;

        case 6:
            cout << "SAT";
            break;
    
        case 7:
            cout << "SUN";
            break;

    }
    return 0;
}